    private void cargarTabla() {
        //Definimos el modelo de la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel() {

            //fila y columna no son editables
            @Override
            public boolean isCellEditable(int row, int colum) {
                return false;
            }
        };

        //establecemos los nombres de las columnas
        String titulos[] = {"Num", "Nombre", "Raza", "Alergico", "At. Esp.", "Dueño", "Cel"};
        modeloTabla.setColumnIdentifiers(titulos);

        //carga de los datos desde la bdd
        List<Mascota> listaMascotas = control.traerMascotas();

        //recorrer la lista y mostrar cada uno de los elementos en la tabla
        if (listaMascotas != null) {
            for (Mascota masco : listaMascotas) {
                Object[] objeto = {masco.getNum_cliente(), masco.getNombre(), masco.getRaza(), masco.getAlergico(),
                    masco.getAtencion_especial(), masco.getUnDuenio().getNombre(), masco.getUnDuenio().getCelDuenio()

                };
                modeloTabla.addRow(objeto);
            }
        }
        tablaMascotas.setModel(modeloTabla);
    }

